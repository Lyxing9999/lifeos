import 'package:flutter_test/flutter_test.dart';
import 'package:flutter_riverpod/flutter_riverpod.dart';
import 'package:shared_preferences/shared_preferences.dart';

import 'package:lifeos_mobile/app/bootstrap.dart';
import 'package:lifeos_mobile/core/storage/shared_prefs_provider.dart';

void main() {
  testWidgets('AppBootstrap renders', (WidgetTester tester) async {
    SharedPreferences.setMockInitialValues({});
    final prefs = await SharedPreferences.getInstance();

    await tester.pumpWidget(
      ProviderScope(
        overrides: [sharedPreferencesProvider.overrideWithValue(prefs)],
        child: const AppBootstrap(),
      ),
    );

    expect(find.byType(AppBootstrap), findsOneWidget);
  });
}
